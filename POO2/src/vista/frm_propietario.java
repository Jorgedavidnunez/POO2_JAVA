    private void btn_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editarActionPerformed
        // TODO add your handling code here:
        obj_propietario.setNit(txt_nit.getText());
        double cui = Double.parseDouble(txt_cui.getText());
        obj_propietario.setCui(cui);
        obj_propietario.setNombres(txt_nombres.getText());
        obj_propietario.setApellidos(txt_apellidos.getText());
        obj_propietario.setDireccion(txt_direccion.getText());
        int tel = Integer.parseInt(txt_telefono.getText());
        obj_propietario.setTelefono(tel);
        String fecha = (txt_dia.getText()+"-"+txt_mes.getText()+"-"+txt_anio.getText());
        obj_propietario.setFecha_nacimiento(fecha);
        obj_propietario.setNo_tarjeta(txt_tarjeta.getText());
        obj_propietario.setUso(txt_uso.getText());
        obj_propietario.setPlaca(txt_placa.getText());
        obj_propietario.setChasis(txt_chasis.getText());
        obj_propietario.setSerie(txt_serie.getText());
        obj_propietario.setMotor(txt_motor.getText());
        obj_propietario.setCilindros(txt_cilindros.getText());
        obj_propietario.setMarca(txt_marca.getText());
        obj_propietario.setModelo(txt_modelo.getText());
        obj_propietario.setLinea(txt_linea.getText());
        obj_propietario.setAsientos(txt_asientos.getText());
        obj_propietario.setTipo(txt_tipo.getText());
        obj_propietario.agregar();
        int nac = Integer.parseInt(txt_anio.getText());
        obj_propietario.edad(nac);
        obj_propietario.operation(cmb_operacion.getSelectedItem().toString());
    }//GEN-LAST:event_btn_editarActionPerformed

